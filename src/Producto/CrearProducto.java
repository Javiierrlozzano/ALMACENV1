    private void SubCodes(java.awt.event.KeyEvent evt){

       
        if (evt.getKeyCode() ==10) {
        String datos = SubCode.getText();
                    if ( ActualizarTabla) {
        DefaultTableModel  modelo ;
        modelo =(DefaultTableModel) TabSubCodes.getModel();
        modelo.removeRow( TabSubCodes.getSelectedRow());
        Insertarables();    
         ActualizarTabla = false;
        }else
        if (datos.length()!=0) {
            boolean Act=true;
            try { 
                int colums  =TabSubCodes.getRowCount();
                for (int i = 0; i < colums; i++) {
                     String valor = TabSubCodes.getValueAt(i, 0).toString();
                     if (SubCode.getText() == null ? valor == null : SubCode.getText().equals(valor)) {
                       Act =false;
                       break;
                    }
                }
            ResultSet resultado=Querys.BuscarSubCodeInicio(datos);
                if (Act) {
                    if (!resultado.next()) {
                        Object Arys[] ={datos,CantSub.getText(),true};
                        general.AgregarDatosJTableArry(TabSubCodes, Arys);
                        SubCode.setText("");
                        CantSub.setText("1.0");  
            }else{JOptionPane.showMessageDialog(jPanel8, "Este codigo ya esta creado Base de datos");}
                }
            else{
            JOptionPane.showMessageDialog(jPanel8, "Este codigo ya esta creado ");
            }}catch(Exception ex ){
            JOptionPane.showConfirmDialog(null, "Error sub cod "+ex.getMessage());
            }
            }else{
             JOptionPane.showMessageDialog(jPanel8, "Debes llenar todos los datos");
         }  
        }
}
