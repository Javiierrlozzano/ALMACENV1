private float ImpuestoIVA(float Bs){
float Impueso= 0;
       if (Impuesto.isEnabled()) {
            float imps = 0;
           String Impuestos =Impuesto.getSelectedItem().toString();
        ResultSet resultado =Querys.Impuestos(Impuestos);
         if ("Seleccionar".equals(Impuestos) ) {
            Impueso =0;
        }else{
          try{
              while(resultado.next()){
                  System.out.println(resultado.getFloat(1));
                  imps = resultado.getFloat(1);
              }
              Impueso = (imps/100)* Bs; 
              ValorImpuesto.setText(Float.toString(Impueso));
          }catch(Exception ex){
              System.out.println("Error "+ex.getMessage());
          }}
            }
return Impueso;
}
