   public void Insert(){
          String datos = CodigoPadre.getText();
        if (datos.length()!=0) {
            try { 
            ResultSet resultado=Querys.BuscarSubCodeInicio(datos);
           if (resultado.next()) {
            Object Arys[] ={datos,resultado.getString(1),CantSubCods.getText(),true};
            general.AgregarDatosJTableArry(TableCombos, Arys);
            CodigoPadre.setText("");
            CantSubCods.setText("1.0"); 
            CodigoPadre.setEditable(true);
            }else{
            JOptionPane.showMessageDialog(jPanel8, "Este codigo ya esta creado en la ");
            }}catch(Exception ex ){
            JOptionPane.showConfirmDialog(null, "Error sub cod "+ex.getMessage());
            }


            }else{
             JOptionPane.showMessageDialog(jPanel8, "Debes llenar todos los datos");
         }
   }
    public void InsertarDatosCombobox(){
      String datos=CodigoPadre.getText();
           if (datos.length()!=0) {
            boolean Act=true;
                int colums  =TableCombos.getRowCount();
                for (int i = 0; i < colums; i++) {
                     String valor = TableCombos.getValueAt(i, 0).toString();
                     if (CodigoPadre.getText() == null ? valor == null : CodigoPadre.getText().equals(valor)) {
                       Act =false;
                       break;
                    }
                }
        if (Act) {
        if ( ActualizarTablaCombos) {
            DefaultTableModel  modelo ;
            modelo =(DefaultTableModel) TableCombos.getModel();
            modelo.removeRow( TableCombos.getSelectedRow());
            Insert();   
            ActualizarTablaCombos = false;}
        else{
            Insert();
        }
                }else{
        JOptionPane.showMessageDialog(null,"Ya esta en la Tabla Combos");
        }
      }
        
        
        
    }
